//将数字按序插入数组
//有一个已排好序的数组{1,4,6,9,13,16,19,28,40,100}，要求输入一个数后，按原来排序的规律将它插入数组中。
#include <stdio.h>
int main()
{
	int x, i, t, j;
	scanf("%d", &x);
	int a[11] = { x,1,4,6,9,13,16,19,28,40,100 };   //输入11个数字，把第一个数字放前面
	for (i = 0;i < 10;i++)    //比较10次，循环10次
		if (a[i]>a[i+1])
        {
			t = a[i];     //大的和小的交换顺序
			a[i] = a[i + 1];
			a[i + 1] = t;
        }
	for (i = 0;i < 11;i++)
		printf("%d ", a[i]);
	return 0;
}